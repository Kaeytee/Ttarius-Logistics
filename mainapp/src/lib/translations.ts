export type Language = keyof typeof translations;
export type TranslationKey = keyof typeof translations.en;

export function getTranslation(language: Language, key: TranslationKey): string {
  return translations[language]?.[key] || translations.en[key] || key;
}

// Hook for using translations in components
import { useState, useEffect } from 'react';

export function useTranslation() {
  const [language, setLanguage] = useState<Language>(() => {
    return (localStorage.getItem('selectedLanguage') as Language) || 'en';
  });

  // Listen for language changes
  useEffect(() => {
    const handleLanguageChange = () => {
      const newLanguage = (localStorage.getItem('selectedLanguage') as Language) || 'en';
      setLanguage(newLanguage);
    };

    // Listen for storage changes (when language is changed in another tab/component)
    window.addEventListener('storage', handleLanguageChange);
    
    // Custom event for same-page language changes
    window.addEventListener('languageChanged', handleLanguageChange);

    // Cleanup
    return () => {
      window.removeEventListener('storage', handleLanguageChange);
      window.removeEventListener('languageChanged', handleLanguageChange);
    };
  }, []);

  const t = (key: TranslationKey): string => {
    return getTranslation(language, key);
  };

  // Function to update language
  const updateLanguage = (newLanguage: Language) => {
    localStorage.setItem('selectedLanguage', newLanguage);
    setLanguage(newLanguage);
    // Dispatch custom event to notify other components
    window.dispatchEvent(new Event('languageChanged'));
  };

  return { t, language, setLanguage: updateLanguage };
}